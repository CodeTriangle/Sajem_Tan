var listWordsProps = function() {
  var words = {};
  var path = [];
  var doObj = function(obj) {
    for (key in obj) {
      if (obj[key].constructor == Array) {
        var psh = getProps(obj[key]);
        psh.path = JSON.parse(JSON.stringify(path));
        if (words.hasOwnProperty(key)) {
          words[key].push(psh);
        } else {
          words[key] = [psh];
        }
      } else {
        path.push(key);
        doObj(obj[key]);
        path.pop();
      }
    }
  };
  doObj(lexicon);
  return words;
};
